
    public class Demo050 {
        public static void main(String[] args) {
            int num = 5;
            int limit = 10;
    
            System.out.println("Multiplication Table of " + num + ":");
            for (int i = 1; i <= limit; i++) {
                System.out.println(num + " * " + i + " = " + (num * i));
            }
        }
    }

